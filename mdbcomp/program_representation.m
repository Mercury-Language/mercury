        var_flag_byte(VarNumRepPrime,
            IncludeVarNameTablePrime, IncludeVarTypesPrime, Byte)
    then
        VarNumRep = VarNumRepPrime,
        IncludeVarNameTable = IncludeVarNameTablePrime,
        IncludeVarTypes = IncludeVarTypesPrime
